CREATE TEMP FUNCTION item(name STRING) RETURNS STRING AS (
    CASE
    WHEN name IN ('A', 'X') THEN 'rock'
    WHEN name IN ('B', 'Y') THEN 'paper'
    WHEN name IN ('C', 'Z') THEN 'scissors'
    ELSE 'unknown'
    END
);

CREATE TEMP FUNCTION shapescore(name STRING) RETURNS INT64 AS (
    CASE
    WHEN name = 'rock' THEN 1
    WHEN name = 'paper' THEN 2
    WHEN name = 'scissors' THEN 3
    ELSE 0
    END
);

CREATE TEMP FUNCTION points(other STRING, self STRING) RETURNS INT64 AS (
    CASE
    -- wins
    WHEN other = 'rock' AND self = 'paper' THEN 6
    WHEN other = 'paper' AND self = 'scissor' THEN 6
    WHEN other = 'scissor' AND self = 'rock' THEN 6
    -- loss
    WHEN other = 'rock' AND self = 'scissor' THEN 0
    WHEN other = 'paper' AND self = 'rock' THEN 0
    WHEN other = 'scissor' AND self = 'paper' THEN 0
    -- draw
    ELSE 3
    END
);

WITH raw AS (
    SELECT SPLIT(line, " ") AS move, offset + 1 AS round
    FROM UNNEST(SPLIT(data, "\n")) AS line WITH OFFSET
), moves AS (
    SELECT
        round,
        item(move[OFFSET(0)]) AS other,
        item(move[OFFSET(1)]) AS self,
    FROM raw
), game AS (
    SELECT
        round, other, self,
        shapescore(self) AS shapescore,
        points(other, self) AS points,
        shapescore(self) + points(other, self) AS score
    FROM moves
)

